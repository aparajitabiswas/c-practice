#include<stdio.h>
int main(){
    int row, column, number;
    printf("Enter row:");
    scanf("%d", &row);
    printf("Enter column:");
    scanf("%d", &column);
    for(row=1; row<=number; row++)
    {
        for(column=1; column<=column; column++)
        {  
            printf("*");
        }
        printf("\n");
    }

    return 0;
}